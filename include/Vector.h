#ifndef FOOTBALLSTARS_VECTOR_H
#define FOOTBALLSTARS_VECTOR_H

/**
* Структура описывающая вектор.
* На векторах построены все физические
* процессы.
*/
struct Vector {
    double x; ///< Переменная, хранящая величину вектора по горизонтали.
    double y; ///< Переменная, хранящая величину вектора по вертикали.
};

#endif // FOOTBALLSTARS_VECTOR_H
